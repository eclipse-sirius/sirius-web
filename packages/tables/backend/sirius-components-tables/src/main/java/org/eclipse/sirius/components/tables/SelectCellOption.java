package org.eclipse.sirius.components.tables;

import java.text.MessageFormat;
import java.util.Objects;

import org.eclipse.sirius.components.annotations.Immutable;

/**
 * The select cell option.
 *
 * @author arichard
 */
@Immutable
public final class SelectCellOption {

    private String id;

    private String label;

    private SelectCellOption() {
        // Prevent instantiation
    }

    public String getId() {
        return this.id;
    }

    public String getLabel() {
        return this.label;
    }

    public static Builder newSelectCellOption(String id) {
        return new Builder(id);
    }

    @Override
    public String toString() {
        String pattern = "{0} '{'id: {1}, label: {2}'}'";
        return MessageFormat.format(pattern, this.getClass().getSimpleName(), this.id, this.label);
    }

    /**
     * The builder used to create the select cell option.
     *
     * @author arichard
     */
    @SuppressWarnings("checkstyle:HiddenField")
    public static final class Builder {

        private final String id;

        private String label;

        private Builder(String id) {
            this.id = Objects.requireNonNull(id);
        }

        public Builder label(String label) {
            this.label = Objects.requireNonNull(label);
            return this;
        }

        public SelectCellOption build() {
            SelectCellOption selectOption = new SelectCellOption();
            selectOption.id = Objects.requireNonNull(this.id);
            selectOption.label = Objects.requireNonNull(this.label);
            return selectOption;
        }
    }
}
